      function interpolationT3T4sq(xv,yv)
      implicit none
      real interpolationT3T4sq,xv,yv
      integer i
      integer iopt,m,kx,ky,nxest,nyest,nmax,nx,ny,
     &        lwrk1,lwrk2,kwrk,ier,lwrk
      parameter (m=10000)
      parameter (nmax=210)
      parameter (lwrk1=100000000)
      parameter (lwrk2=50000000)
      parameter (kwrk=70000)
      parameter (lwrk=50)
      integer iwrk(kwrk)
      real x(m),y(m),z(m),w(m),xb,xe,yb,ye,s,eps,
     &   tx(nmax),ty(nmax),c(50000),fp,wrk1(lwrk1),wrk2(lwrk2)
      integer mx,my
      real xx(1),yy(1),zz(1),wrk(lwrk),aux_fac
      common/interpT3T4sq/tx,ty,c,nx,ny,kx,ky

      mx = 1 
      my = 1

      xx(1) = xv
      yy(1) = yv

      call bispev(tx,nx,ty,ny,c,kx,ky,xx,mx,yy,my,zz,wrk,lwrk,
     &  iwrk,kwrk,ier)

      aux_fac = (1.-yy(1)**2.)/(1./xx(1)-yy(1)**2.)
      interpolationT3T4sq = zz(1)*aux_fac**2.

      return
      end

