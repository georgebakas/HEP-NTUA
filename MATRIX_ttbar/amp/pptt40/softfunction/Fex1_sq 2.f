c Routines for the interpolation of the results corresponding
c to <Fex1(0)^2>, written in terms of the color structures
c T3T4^2, T3T4*CF and CF^2

      function Fex1sq_T3T4sq(beta,cost)
      ! This routine returns the contribution to T3T4T3T4
      ! coming from <Fex1(0)^2>
      ! Here beta=sqrt(1-4*mt**2/s)
      implicit none
      double precision Fex1sq_T3T4sq,beta,beta2,cost
      real beta2_r,cost_r,interpolationT3T4sq
      external interpolationT3T4sq

      beta2 = beta*beta
      beta2_r = real(beta2)
      cost_r = real(cost)
      Fex1sq_T3T4sq = dble(interpolationT3T4sq(beta2_r,cost_r))
      Fex1sq_T3T4sq = Fex1sq_T3T4sq*(1.d0+dlog(1.d0-beta2)**2.d0)**2.d0

      return
      end

